package src.Conditions;

public class IfElse {
    public static void main(String[] args) {
        int a=34;

        if(a%2==0){
            System.out.println("Its an even number");

        }
        else {
            System.out.println("Its an odd number");
        }
        if(a%2==1){
            System.out.println("Its an odd number");

        }
        else {
            System.out.println("Its an even number");
        }
    }
}
